    private void updateQuantityAndStatus(int row) {
        try {
            // Get the ID and new quantity from the table
            int id = (int) jTable1.getValueAt(row, 0); // ID column
            int newQuantity = Integer.parseInt(jTable1.getValueAt(row, 2).toString()); // Quantity column

            String newStatus = newQuantity > 0 ? "In Stock" : "Out of Stock";

            // Update the table with the new status
            jTable1.setValueAt(newStatus, row, 4); // Status column

            // Update the database
            try (Connection conn = DatabaseConnection.getConnection()) {
                String sql = "UPDATE inventory SET quantity = ?, status = ? WHERE id = ?";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setInt(1, newQuantity);
                pstmt.setString(2, newStatus);
                pstmt.setInt(3, id);
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Inventory updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error updating inventory in the database.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for quantity.", "Error", JOptionPane.ERROR_MESSAGE);
            // Revert the quantity in the table to the previous value
            populateInventoryTable();
        }
    }

