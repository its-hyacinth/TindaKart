   private void saveToDatabase() {
        String name = nameField.getText();
        String priceStr = priceField.getText();
        String quantityStr = quantityField.getText();

        if (name.isEmpty() || priceStr.isEmpty() || quantityStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double price = Double.parseDouble(priceStr);
            int quantity = Integer.parseInt(quantityStr);

            if (price <= 0 || quantity < 0) {
                JOptionPane.showMessageDialog(this, "Price must be greater than 0 and quantity cannot be negative!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            try (Connection conn = DatabaseConnection.getConnection()) {
                String sql = "INSERT INTO inventory (name, quantity, price, status) VALUES (?, ?, ?, ?)";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, name);
                pstmt.setInt(2, quantity);
                pstmt.setDouble(3, price);
                pstmt.setString(4, "In Stock"); // Default status "available"
                pstmt.executeUpdate();

                // Update display area and reset text fields
//            displayArea.append("Saved: " + name + ", Price: " + price + ", Quantity: " + quantity + "\n");
                nameField.setText("");
                priceField.setText("");
                quantityField.setText("");

                // Refresh the table with updated data
                populateInventoryTable();

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error saving to database!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values for price and quantity!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void populateInventoryTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        try (Connection conn = DatabaseConnection.getConnection()) {
            String sql = "SELECT id, name, quantity, price, status FROM inventory";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();

            // Populate the table with the fetched data
            while (rs.next()) {
                int id = rs.getInt("id");
                String name = rs.getString("name");
                int quantity = rs.getInt("quantity");
                double price = rs.getDouble("price");
                String status = rs.getString("status");

                model.addRow(new Object[]{id, name, quantity, price, status});
            }

            // Set the custom cell renderer for the status column
            jTable1.getColumnModel().getColumn(4).setCellRenderer(new StatusColumnCellRenderer());

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading inventory data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
     private class StatusColumnCellRenderer extends DefaultTableCellRenderer {
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            JLabel label = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            
            String status = (String) value;
            label.setOpaque(true);
            label.setHorizontalAlignment(JLabel.CENTER);
            
            if ("In Stock".equalsIgnoreCase(status)) {
                label.setBackground(new Color(0, 150, 0)); // Green
                label.setForeground(Color.WHITE);
                label.setText("In Stock");
            } else if ("Out of Stock".equalsIgnoreCase(status)) {
                label.setBackground(new Color(150, 0, 0)); // Red
                label.setForeground(Color.WHITE);
                label.setText("Out of Stock");
            } else {
                label.setBackground(table.getBackground());
                label.setForeground(table.getForeground());
            }
            
            return label;
        }
    }

    private void deleteInventoryItem() {
        // Get the selected row index
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an inventory item to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Get the inventory ID from the selected row
        int inventoryId = (int) jTable1.getValueAt(selectedRow, 0); // Assuming ID is in the first column

        // Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this item?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = DatabaseConnection.getConnection()) {
                String sql = "DELETE FROM inventory WHERE id = ?";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setInt(1, inventoryId);
                pstmt.executeUpdate();

                // Refresh the table after deletion
                populateInventoryTable();
                JOptionPane.showMessageDialog(this, "Inventory item deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error deleting inventory item!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

